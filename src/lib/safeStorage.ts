/**
 * Safe localStorage wrapper that falls back to in-memory storage
 * when localStorage is unavailable (e.g., Safari private browsing mode).
 */

class MemoryStorage implements Storage {
  private store = new Map<string, string>();

  get length(): number {
    return this.store.size;
  }

  clear(): void {
    this.store.clear();
  }

  getItem(key: string): string | null {
    return this.store.get(key) ?? null;
  }

  key(index: number): string | null {
    const keys = Array.from(this.store.keys());
    return keys[index] ?? null;
  }

  removeItem(key: string): void {
    this.store.delete(key);
  }

  setItem(key: string, value: string): void {
    this.store.set(key, value);
  }
}

function isLocalStorageAvailable(): boolean {
  try {
    const testKey = '__storage_test__';
    localStorage.setItem(testKey, 'test');
    localStorage.removeItem(testKey);
    return true;
  } catch {
    return false;
  }
}

/**
 * Returns localStorage if available, otherwise returns an in-memory fallback.
 * Safe for Safari private browsing and other restricted environments.
 */
export const safeStorage: Storage = isLocalStorageAvailable()
  ? localStorage
  : new MemoryStorage();
